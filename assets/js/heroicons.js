// Function to load icons on page load
function loadHeroicons() {
  try {
    // Find all elements with hero- classes
    const heroElements = document.querySelectorAll('[class*="hero-"]');
    
    heroElements.forEach(element => {
      try {
        // Extract icon name from class - handle both string and SVGAnimatedString
        let classNameStr = '';
        if (typeof element.className === 'string') {
          classNameStr = element.className;
        } else if (element.className && element.className.baseVal && typeof element.className.baseVal === 'string') {
          classNameStr = element.className.baseVal;
        } else {
          return; // Skip if no valid className
        }
        
        const classes = classNameStr.split(' ');
        const heroClass = classes.find(cls => cls.startsWith('hero-'));
        
        if (heroClass) {
          const iconName = heroClass.replace('hero-', '');
          const svgContent = iconMap[iconName];
          
          if (svgContent && element.innerHTML.trim() === '') {
            // Set the SVG content
            element.innerHTML = svgContent;
            element.style.display = 'inline-flex';
            element.style.alignItems = 'center';
            element.style.justifyContent = 'center';
          }
        }
      } catch (e) {
        console.debug('Icon loading: element processing error', e);
      }
    });
  } catch (e) {
    console.debug('Icon loading: general error', e);
  }
}

// Function to handle dynamically added icons
function observeIconChanges() {
  const observer = new MutationObserver((mutations) => {
    try {
      mutations.forEach((mutation) => {
        if (mutation.type === 'childList') {
          mutation.addedNodes.forEach((node) => {
            if (node.nodeType === Node.ELEMENT_NODE) {
              // Check if the added node or its children have hero- classes
              const heroElements = node.querySelectorAll ? 
                node.querySelectorAll('[class*="hero-"]') : [];
              
              // Check the node itself for hero- classes
              try {
                if (node.className) {
                  if (typeof node.className === 'string' && node.className.includes('hero-')) {
                    loadSingleIcon(node);
                  } else if (node.className.baseVal && typeof node.className.baseVal === 'string' && node.className.baseVal.includes('hero-')) {
                    loadSingleIcon(node);
                  }
                }
              } catch (e) {
                // Silently ignore className access errors
                console.debug('Icon loading: className access error', e);
              }
              
              // Process child elements
              heroElements.forEach(element => {
                try {
                  loadSingleIcon(element);
                } catch (e) {
                  console.debug('Icon loading: child element error', e);
                }
              });
            }
          });
        }
      });
    } catch (e) {
      console.debug('Icon loading: mutation observer error', e);
    }
  });
  
  observer.observe(document.body, {
    childList: true,
    subtree: true
  });
}

